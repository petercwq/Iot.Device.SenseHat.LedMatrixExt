    0x78 => 0111 1000 => .XXXX...
    0xCC => 1100 1100 => XX..XX..
    0xCC => 1100 1100 => XX..XX..
    0xFC => 1111 1100 => XXXXXX..
    0xCC => 1100 1100 => XX..XX..
    0xCC => 1100 1100 => XX..XX..
    0x00 => 0000 0000 => ........

To access the nth pixel in a row, right-shift by 7-n.

                         . . X X . . . .
                         | | | | | | | |
    (0x30 >> 7) & 1 == 0-+ | | | | | | |
    (0x30 >> 6) & 1 == 0---+ | | | | | |
    (0x30 >> 5) & 1 == 1-----+ | | | | |
    (0x30 >> 4) & 1 == 1-------+ | | | |
    (0x30 >> 3) & 1 == 0---------+ | | |
    (0x30 >> 2) & 1 == 0-----------+ | |
    (0x30 >> 1) & 1 == 0-------------+ |
